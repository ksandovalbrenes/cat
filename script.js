function drawContainer(matrix, currentMatrix){
    for (let i = 0; i < matrix.length; i++){
        for (let j = 0; j < matrix[i].length; j++){
            if (matrix[i][j] != 0 || currentMatrix[i][j] != 0){
                drawPixel(i, j, matrix[i][j], elements);
            } 
        }
    }
}

function drawPixel(i, j, x, pixels){
    let position = j + (i * 40);
    pixels[position].style.backgroundColor = getPixelColor(x);
}

function getPixelColor(x){
    switch (x) {
        case 0:
            //white
            return "#f0eded";
        case 1:
            //Gray 1
            return "#3D3D3D";
        case 2:
            //Gray 2
            return "#616161";
        case 3:
            //Gray 3
            return "#B8B8B8";
    }

}

function sleep(ms){
    return new Promise(resolve => setTimeout(resolve, ms));
}

